<template>
  <button
    ref="btn"
    :type="props.type"
    :disabled="disabled || loading"
    :class="[getClass(), props.class]"
    @click="props.type === 'button' ? emit('click') : null"
  >
    <svg
      v-if="loading"
      class="mr-2 -ml-1 w-5 h-5 text-white animate-spin"
      xmlns="http://www.w3.org/2000/svg"
      fill="none"
      viewBox="0 0 24 24"
    >
      <circle
        class="opacity-25"
        cx="12"
        cy="12"
        r="10"
        stroke="currentColor"
        stroke-width="4"
      />
      <path
        class="opacity-75"
        fill="currentColor"
        d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z"
      />
    </svg>

    <slot
      v-if="!loading"
      name="prepend"
    />
    <slot />
    <slot
      name="append"
    />
  </button>
</template>

<script setup>
const btn = ref();
const props = defineProps({
  color: { type: String, default: 'indigo' },
  type: { type: String, default: 'button' },
  loading: { type: Boolean, default: false },
  disabled: { type: Boolean, default: false },
  class: { type: String, default: null },
  addClass: { type: String, default: null },
});
const emit = defineEmits(['click']);

const getColors = () => `btn-${props.color}`;

const getClass = () => {
  let classes = 'btn-default ';

  if (props.loading) {
    classes += ' btn-loading ';
  } else if (props.disabled) {
    classes += ' btn-disabled ';
  }

  classes += getColors();

  if (props.addClass !== null) {
    classes += ` ${props.addClass}`;
  }

  return classes;
};

const setFocus = () => {
  if (btn.value) {
    btn.value.focus();
  }
};

defineExpose({ setFocus });
</script>
